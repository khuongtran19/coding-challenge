const multArray = require("./multipleArray");


test.each(
    [
        [[5, 8, 2, 4, 1], [64, 40, 160, 80, 320]],
        [[12, 4, 47, 56, 28], [294784, 884352, 75264, 63168, 126336]],
        [[1423, 545643, 78323, 5428892, 342534], [7.947175250069666e+22, 207256949706110660000, 1.4438709422326945e+21, 20830825849637704000, 330152054419390000000]],
        [[32, 94, 55, 73, 11], [4151510, 1413280, 2415424, 1819840, 12077120]]
    ]
)
    ('Given array [%s] the result should be [%s]', (given, expected) => {
        expect(multArray(given)).toEqual(expect.arrayContaining(expected));
    })